#include "libft.h"

size_t	ft_wordcount(char const *s, char c)
{
	size_t		i;

	i = 0;
	if (*s && *s != c)
		i++;
	while (*++s)
		if (*s != c && *(s - 1) == c)
			i++;
	return (i);
}
