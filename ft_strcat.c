#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*st1;
	char	*st2;

	st1 = s1;
	st2 = (char *)s2;
	while (*st1)
	{
		st1++;
	}
	while (*st2)
	{
		*st1 = *st2;
		st1++;
		st2++;
	}
	*st1 = '\0';
	return (s1);
}
