#include "libft.h"

size_t	ft_nblen(int nb, int bs)
{
	size_t	len;

	len = 0;
	if (nb == 0)
		return (1);
	if (nb < 0 && bs == 10)
		len += 1;
	while (nb)
	{
		nb /= bs;
		len += 1;
	}
	return (len);
}
