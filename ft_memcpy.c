#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*p1;
	char	*p2;
	size_t	i;

	p1 = dst;
	p2 = (char *)src;
	i = -1;
	while (++i < n)
	{
		*(p1 + i) = *(p2 + i);
	}
	return (dst);
}
