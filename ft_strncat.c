#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	l;

	i = 0;
	l = ft_strlen(s1);
	while ((i < n) && (s2[i]))
	{
		s1[i + l] = s2[i];
		i = i + 1;
	}
	s1[i + l] = '\0';
	return (s1);
}
